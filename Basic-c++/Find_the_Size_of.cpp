# include <iostream>

using namespace std;

int main()
{
    char charType;
    int integerType;
    float floatType;
    double doubleType;
    
    cout << "size of char: " << sizeof(charType) << endl;
    cout << "size of int: " << sizeof(integerType) << endl;
    cout << "size of float: " << sizeof(floatType) << endl;
    cout << "size of double: " << sizeof(doubleType) << endl;

    return 0;
}